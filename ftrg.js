
function chk(num){

    switch(true){
    case (num>0):{
    console.log("Number"+num+" is postive");
    break;
    }
    case(num==0):{
    console.log("Number"+num+" is zero");
    break;
   
    }
    case (num<0):{
        console.log("Number"+num+" is negative");
        break;
        
    }default:{
    console.log("Input is invalid");}
}
}


chk(4);
chk(0);
chk(-15);
chk("Abhimanyu Singh");





























